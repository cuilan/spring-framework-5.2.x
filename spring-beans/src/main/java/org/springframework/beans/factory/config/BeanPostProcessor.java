package org.springframework.beans.factory.config;

import org.springframework.beans.BeansException;
import org.springframework.lang.Nullable;

/**
 * BeanPostProcessor是Spring框架的一个扩展点（不止一个）
 * 通过实现BeanPostProcessor接口，程序员可以干预bean的实例化的过程，从而减轻了BeanFactory的负担，
 * 这个接口可以设置多个，会形成一个列表，然后依次执行。
 * 比如，AOP就是在bean实例化后期将切面逻辑织入bean实例中的，AOP也正是通过BeanPostProcessor和IOC容器建立联系的
 * （由Spring提供的默认的PostProcessor，Spring提供了很多默认的PostProcessor的实现类）。
 * 实现类：
 * 1.ApplicationContextAwareProcessor {@link org.springframework.context.support.ApplicationContextAwareProcessor}
 * 2.InitDestroyAnnotationBeanPostProcessor {@link org.springframework.beans.factory.annotation.InitDestroyAnnotationBeanPostProcessor}
 * 3.InstantiationAwareBeanPostProcessor {@link org.springframework.beans.factory.config.InstantiationAwareBeanPostProcessor}
 * 4.CommonAnnotationBeanPostProcessor {@link org.springframework.context.annotation.CommonAnnotationBeanPostProcessor}
 * 5.AutowiredAnnotationBeanPostProcessor {@link org.springframework.beans.factory.annotation.AutowiredAnnotationBeanPostProcessor}
 * 6.RequiredAnnotationBeanPostProcessor {@link org.springframework.beans.factory.annotation.RequiredAnnotationBeanPostProcessor}
 * 7.BeanValidationPostProcessor {@link org.springframework.validation.beanvalidation.BeanValidationPostProcessor}
 * 8.AbstractAutoProxyCreator {@link org.springframework.aop.framework.autoproxy.AbstractAutoProxyCreator}
 * 9.BeanFactoryPostProcessor {@link org.springframework.beans.factory.config.BeanFactoryPostProcessor}
 * 10.ConfigurableListableBeanFactory {@link org.springframework.beans.factory.config.ConfigurableListableBeanFactory}
 * ...
 * <p>
 * Factory hook that allows for custom modification of new bean instances &mdash;
 * for example, checking for marker interfaces or wrapping beans with proxies.
 *
 * <p>Typically, post-processors that populate beans via marker interfaces
 * or the like will implement {@link #postProcessBeforeInitialization},
 * while post-processors that wrap beans with proxies will normally
 * implement {@link #postProcessAfterInitialization}.
 *
 * <h3>Registration</h3>
 * <p>An {@code ApplicationContext} can autodetect {@code BeanPostProcessor} beans
 * in its bean definitions and apply those post-processors to any beans subsequently
 * created. A plain {@code BeanFactory} allows for programmatic registration of
 * post-processors, applying them to all beans created through the bean factory.
 *
 * <h3>Ordering</h3>
 * <p>{@code BeanPostProcessor} beans that are autodetected in an
 * {@code ApplicationContext} will be ordered according to
 * {@link org.springframework.core.PriorityOrdered} and
 * {@link org.springframework.core.Ordered} semantics. In contrast,
 * {@code BeanPostProcessor} beans that are registered programmatically with a
 * {@code BeanFactory} will be applied in the order of registration; any ordering
 * semantics expressed through implementing the
 * {@code PriorityOrdered} or {@code Ordered} interface will be ignored for
 * programmatically registered post-processors. Furthermore, the
 * {@link org.springframework.core.annotation.Order @Order} annotation is not
 * taken into account for {@code BeanPostProcessor} beans.
 *
 * @author Juergen Hoeller
 * @author Sam Brannen
 * @see InstantiationAwareBeanPostProcessor
 * @see DestructionAwareBeanPostProcessor
 * @see ConfigurableBeanFactory#addBeanPostProcessor
 * @see BeanFactoryPostProcessor
 * @since 10.10.2003
 */
public interface BeanPostProcessor {

	/**
	 * Apply this {@code BeanPostProcessor} to the given new bean instance <i>before</i> any bean
	 * initialization callbacks (like InitializingBean's {@code afterPropertiesSet}
	 * or a custom init-method). The bean will already be populated with property values.
	 * The returned bean instance may be a wrapper around the original.
	 * <p>The default implementation returns the given {@code bean} as-is.
	 *
	 * @param bean     the new bean instance
	 * @param beanName the name of the bean
	 * @return the bean instance to use, either the original or a wrapped one;
	 * if {@code null}, no subsequent BeanPostProcessors will be invoked
	 * @throws org.springframework.beans.BeansException in case of errors
	 * @see org.springframework.beans.factory.InitializingBean#afterPropertiesSet
	 */
	@Nullable
	default Object postProcessBeforeInitialization(Object bean, String beanName) throws BeansException {
		return bean;
	}

	/**
	 * Apply this {@code BeanPostProcessor} to the given new bean instance <i>after</i> any bean
	 * initialization callbacks (like InitializingBean's {@code afterPropertiesSet}
	 * or a custom init-method). The bean will already be populated with property values.
	 * The returned bean instance may be a wrapper around the original.
	 * <p>In case of a FactoryBean, this callback will be invoked for both the FactoryBean
	 * instance and the objects created by the FactoryBean (as of Spring 2.0). The
	 * post-processor can decide whether to apply to either the FactoryBean or created
	 * objects or both through corresponding {@code bean instanceof FactoryBean} checks.
	 * <p>This callback will also be invoked after a short-circuiting triggered by a
	 * {@link InstantiationAwareBeanPostProcessor#postProcessBeforeInstantiation} method,
	 * in contrast to all other {@code BeanPostProcessor} callbacks.
	 * <p>The default implementation returns the given {@code bean} as-is.
	 *
	 * @param bean     the new bean instance
	 * @param beanName the name of the bean
	 * @return the bean instance to use, either the original or a wrapped one;
	 * if {@code null}, no subsequent BeanPostProcessors will be invoked
	 * @throws org.springframework.beans.BeansException in case of errors
	 * @see org.springframework.beans.factory.InitializingBean#afterPropertiesSet
	 * @see org.springframework.beans.factory.FactoryBean
	 */
	@Nullable
	default Object postProcessAfterInitialization(Object bean, String beanName) throws BeansException {
		return bean;
	}

}
